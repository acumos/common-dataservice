package org.acumos.cds.repository;

import javax.transaction.Transactional;

import org.acumos.cds.domain.MLPSolRevDocMap;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.repository.CrudRepository;
import org.springframework.data.repository.query.Param;

public interface SolRevDocMapRepository extends CrudRepository<MLPSolRevDocMap, MLPSolRevDocMap.SolRevDocMapPK> {

	/**
	 * Finds map entries for the specified revision ID.
	 * 
	 * Generated by Spring magic.
	 * 
	 * Not defined as pageable because the number of revisions for a solution is
	 * expected to be modest.
	 * 
	 * @param revisionId
	 *                       revision ID
	 * @return Iterable of MLPSolutionRevision
	 */
	Iterable<MLPSolRevDocMap> findByRevisionId(@Param("revisionId") String revisionId);

	/**
	 * Deletes all entries for the specified revision ID
	 * 
	 * Generated by Spring magic.
	 * 
	 * @param revisionId
	 *                       Revision ID
	 */
	@Modifying
	@Transactional // throws exception without this
	void deleteByRevisionId(@Param("revisionId") String revisionId);

}
